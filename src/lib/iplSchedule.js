/**
 * Get upcoming matches (from today onwards) that haven't been created yet.
 * @param {Array} existingMatchNames - Names of matches already created in the league
 * @returns {Array} Matches not yet created
 */
export function getUpcomingMatches(existingMatchNames = []) {
  const today = new Date().toISOString().split('T')[0]
  const existingSet = new Set(existingMatchNames.map((n) => n.toLowerCase()))
  return IPL_2026_SCHEDULE.filter(
    (m) => m.date >= today && !existingSet.has(m.teams.toLowerCase())
  )
}

/**
 * Get all matches not yet created in the league.
 */
export function getMissingMatches(existingMatchNames = []) {
  const existingSet = new Set(existingMatchNames.map((n) => n.toLowerCase()))
  return IPL_2026_SCHEDULE.filter(
    (m) => !existingSet.has(m.teams.toLowerCase())
  )
}
